const bilingual = (vi, en = vi) => ({ vi, en });

function createWord(
  id,
  term,
  phonetic,
  meaningVi,
  meaningEn,
  partOfSpeech,
  exampleVi,
  exampleEn,
  known = false,
) {
  return {
    id,
    term,
    phonetic,
    meaning: bilingual(meaningVi, meaningEn),
    partOfSpeech,
    example: bilingual(exampleVi, exampleEn),
    known,
  };
}

function createWords(prefix, items) {
  return items.map(
    (
      [
        term,
        phonetic,
        meaningVi,
        meaningEn,
        partOfSpeech,
        exampleVi,
        exampleEn,
        known = false,
      ],
      index,
    ) =>
      createWord(
        `${prefix}-${index + 1}`,
        term,
        phonetic,
        meaningVi,
        meaningEn,
        partOfSpeech,
        exampleVi,
        exampleEn,
        known,
      ),
  );
}

function createSet({
  id,
  titleVi,
  titleEn = titleVi,
  wordCount,
  learnedCount = 0,
  locked = false,
  pro = false,
  words = [],
}) {
  const resolvedWordCount = words.length > 0 ? words.length : wordCount;
  const resolvedLearnedCount =
    words.length > 0 ? words.filter((word) => word.known).length : learnedCount;

  return {
    id,
    title: bilingual(titleVi, titleEn),
    wordCount: resolvedWordCount,
    learnedCount: resolvedLearnedCount,
    locked,
    pro,
    words,
  };
}

function createTrack({
  id,
  group,
  accent,
  titleVi,
  titleEn = titleVi,
  difficulty,
  sets,
}) {
  const totalWords = sets.reduce((sum, set) => sum + set.wordCount, 0);
  const learnedWords = sets.reduce((sum, set) => sum + set.learnedCount, 0);
  const completion = totalWords ? Math.round((learnedWords / totalWords) * 100) : 0;

  return {
    id,
    group,
    accent,
    title: bilingual(titleVi, titleEn),
    difficulty,
    totalWords,
    learnedWords,
    completion,
    setCount: sets.length,
    sets,
  };
}

